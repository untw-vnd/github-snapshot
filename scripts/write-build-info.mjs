#!/usr/bin/env node
/**
 * Generate src/build-info.ts with the current git commit and branch.
 *
 * Reads from Workers Builds CI environment variables when present, falls
 * back to invoking git locally. Writes "unknown" if neither works (e.g. a
 * shallow checkout without git in $PATH).
 *
 * Run automatically as the prebuild step. The output file is gitignored.
 */

import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

function tryGit(args) {
  try {
    return execSync(`git ${args}`, {
      cwd: root,
      stdio: ["ignore", "pipe", "ignore"],
    })
      .toString()
      .trim();
  } catch {
    return null;
  }
}

const commit =
  process.env.WORKERS_CI_COMMIT_SHA ?? tryGit("rev-parse HEAD") ?? "unknown";

const branch =
  process.env.WORKERS_CI_BRANCH ??
  tryGit("rev-parse --abbrev-ref HEAD") ??
  "unknown";

const contents = `// Auto-generated by scripts/write-build-info.mjs. Do not edit by hand.
export const BUILD_INFO = {
	commit: ${JSON.stringify(commit)},
	branch: ${JSON.stringify(branch)},
} as const;
`;

writeFileSync(join(root, "src", "build-info.ts"), contents);
console.log(`Wrote src/build-info.ts (commit=${commit}, branch=${branch})`);
